package ex05;

public class OperEx01 {
    public static void main(String[] args) { // 기본 산술 연산자
        System.out.println(1 + 1);
        System.out.println(1 - 1);
        System.out.println(2 * 2);
        System.out.println(3 % 2); // 나머지 (모드 연산자 꼭 기억하기 : 짝수인지 홀수인지를 알고싶을 때)

        // 더하기 (산술더하기 or 결합)
        System.out.println("1" + 1); // 쌍따옴표로 감싸지 않으면 문자로 보지않는다. 양쪽의 타입이 같아야 함
    }
}
